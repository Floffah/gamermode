package dev.floffah.gamermode.nbt.tags;

/**
 * All NBT Types
 */
public enum NBTType {
    /**
     * NBT End tag (0)
     */
    END,
    /**
     * NBT Byte tag (1)
     */
    BYTE,
    /**
     * NBT Short tag (2)
     */
    SHORT,
    /**
     * NBT Int tag (3)
     */
    INT,
    /**
     * NBT Long tag (4)
     */
    LONG,
    /**
     * NBT Float tag (5)
     */
    FLOAT,
    /**
     * NBT Double tag (6)
     */
    DOUBLE,
    /**
     * NBT Byte_Array tag (7)
     */
    BYTE_ARRAY,
    /**
     * NBT String tag (8)
     */
    STRING,
    /**
     * NBT List tag (9)
     */
    LIST,
    /**
     * NBT Compound tag (10)
     */
    COMPOUND,
    /**
     * NBT Int_Array tag (11)
     */
    INT_ARRAY,
    /**
     * NBT Long_Array tag (12)
     */
    LONG_ARRAY
}
